package kotlinlab.basic

/**
 * Handling basic arithmetic operations in Kotlin
 */

fun main(){
    val num0 = 5
    val num1 = 2.0
    println("num0 + num1 = ${num0 + num1}")
    println("num0 - num1 = ${num0 - num1}")
    println("num0 * num1 = ${num0 * num1}")
    println("num0 / num1 = ${num0 / num1}")
    println("num0 % num1 = ${num0 % num1}")
}