package kotlinlab.threads.coroutines

/*
Coroutines allow you to create multiple pieces of code that can run
asynchronously. Instead of running pieces of code in sequence, one after the
other, coroutines let you run them side-by-side.

Using coroutines means that you can launch a background job, such as reading
data from an external server, without the rest of your code having to wait for the
job to complete before doing anything else. This gives your user a more fluid
experience, and it also makes your application more scalable.
 */